'''Efetuar a leitura de dois valores numéricos inteiros representados pelas variáveis A e B e apresentar o resultado da diferença A-B.
'''
# Entrada de dados:

A = int(input('Digite um valor inteiro para a variável A: '))
B = int(input('Digite um valor inteiro para a variável B: '))

# Calcula a diferença entre os pontos A e B

R = A - B

# Saída de dados:

print(f'A diferença entre as variáveis A e B é: {R}')
