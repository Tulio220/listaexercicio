'''Faça um algoritmo para calcular quantas ferraduras são necessárias para equipar todos os cavalos comprados para um haras.
'''
#Entrada:
quantidade_cavalos = int(input('Digite a quantidade de cavalos que o haras possue: '))
#Processamento:
resultado = quantidade_cavalos * 4
#Saída:
print(f'A quantidade de ferraduras necessárias é {resultado}')